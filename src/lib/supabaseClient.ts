import { createClient } from "@supabase/supabase-js";

const supabaseUrl = import.meta.env.VITE_SUPABASE_URL as string;
const supabaseAnonKey = import.meta.env.VITE_SUPABASE_ANON_KEY as string;

// Singleton Supabase client for browser usage.
// - persistSession: stores session in localStorage
// - autoRefreshToken: refreshes tokens in the background
// - detectSessionInUrl: finalizes sessions from magic-link/email callbacks
export const supabase = createClient(supabaseUrl, supabaseAnonKey, {
  auth: {
    persistSession: true,
    autoRefreshToken: true,
    detectSessionInUrl: true,
  },
});

